package com.sitemap.qingzangtrain.model;

/**
 * 获取列车实时数据
 * Created by chenmeng on 2017/3/21.
 */

public class CurrentTrainModel {
    private String img;//    状态图片；起始/终点 先固定为 "沈阳/拉萨";
    private String tn;//    车次组；
    private String jianKong;//                监控车；
    private String zaixian;//                在线
    private String carriageSize;//            编组；
    private String quJian;//                运行区间；
    private String lon;//                    为0时GPS状态故障其他值正常；
    private String speed;//            速度；
    private String time;//                                        最新上报时间 
    private String lateTime;//                                晚点时间；
    private String weiHu;//                            维护建议
    private String state;//状态类别

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTn() {
        return tn;
    }

    public void setTn(String tn) {
        this.tn = tn;
    }

    public String getJianKong() {
        return jianKong;
    }

    public void setJianKong(String jianKong) {
        this.jianKong = jianKong;
    }

    public String getZaixian() {
        return zaixian;
    }

    public void setZaixian(String zaixian) {
        this.zaixian = zaixian;
    }

    public String getCarriageSize() {
        return carriageSize;
    }

    public void setCarriageSize(String carriageSize) {
        this.carriageSize = carriageSize;
    }

    public String getQuJian() {
        return quJian;
    }

    public void setQuJian(String quJian) {
        this.quJian = quJian;
    }

    public String getLon() {
        return lon;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }

    public String getSpeed() {
        return speed;
    }

    public void setSpeed(String speed) {
        this.speed = speed;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getLateTime() {
        return lateTime;
    }

    public void setLateTime(String lateTime) {
        this.lateTime = lateTime;
    }

    public String getWeiHu() {
        return weiHu;
    }

    public void setWeiHu(String weiHu) {
        this.weiHu = weiHu;
    }
}
